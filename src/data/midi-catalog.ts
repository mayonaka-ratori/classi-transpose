/** Flat list of every piece for id-based lookup */
export const ALL_PIECES: MidiPiece[] = MIDI_CATALOG.flatMap((g) => g.pieces);

/** Find a piece by its id */
export function findPieceById(id: string): MidiPiece | undefined {
  return ALL_PIECES.find((p) => p.id === id);
}

/** Filter composers to those that have at least one piece matching the category */
export function getComposersByCategory(category: MidiCategory | 'all'): ComposerGroup[] {
  if (category === 'all') return MIDI_CATALOG;
  return MIDI_CATALOG.filter((g) => g.pieces.some((p) => p.category === category));
}
